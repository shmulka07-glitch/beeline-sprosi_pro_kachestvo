// Нормализация текста для поиска
function normalize(s) {
  return String(s || "")
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/[^\p{L}\p{N}\s-]+/gu, "")
    .trim();
}

// Экранирование HTML для безопасности
function escapeHtml(s) {
  return String(s)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

// Расчет релевантности ответа (ОБНОВЛЕННАЯ ЛОГИКА С КАТЕГОРИЯМИ)
function scoreEntry(entry, queryNorm) {
  if (!queryNorm) return 0;
  let score = 0;
  
  // Поиск по ключевым словам
  const kw = entry.keywords || [];
  for (const k of kw) {
    const nk = normalize(k);
    if (!nk) continue;
    if (queryNorm.includes(nk)) score += 10;
  }
  
  // Поиск по заголовку (больший вес)
  const titleNorm = normalize(entry.title);
  if (titleNorm && queryNorm.includes(titleNorm)) score += 20;
  
  // Поиск по категории (средний вес)
  const catNorm = normalize(entry.category);
  if (catNorm && queryNorm.includes(catNorm)) score += 15;

  // Частичное совпадение по тексту описания и заголовку
  const hay = `${normalize(entry.title)} ${normalize(entry.short)} ${normalize(entry.category)}`;
  const words = queryNorm.split(/\s+/).filter(w => w.length > 3);
  
  for (const w of words) {
    if (hay.includes(w)) score += 2;
  }
  
  return score;
}

